char	*ft_strcpy(char *dest, char *src)
{
	char	*tmp;

	tmp = dest;
	while (*src != '\0')
	{
		*dest = *src;
		src++ ;
		dest++ ;
	}
	*dest = '\0';
	return (tmp);
}
